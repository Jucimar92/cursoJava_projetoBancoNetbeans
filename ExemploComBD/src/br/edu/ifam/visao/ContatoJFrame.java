    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String strId = JOptionPane.showInputDialog(this, "Digite um id ");
        long id = Long.parseLong(strId);
        posicao = 0;
        while (posicao < listaDeContatos.size()) {
            contato = listaDeContatos.get(posicao);
            if (contato.getId() == id) {
                carregaDadosParaTela(contato);
                return;
            }
            posicao++;
        }
        JOptionPane.showMessageDialog(this, "Id pesquisado não existe",
                "Atenção", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnCarregaFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregaFotoActionPerformed
        FileInputStream fis;
        int estado = jfcSelecionaFoto.showOpenDialog(this);

        if (estado == JFileChooser.APPROVE_OPTION) {
            try {
                fis = new FileInputStream(jfcSelecionaFoto.getSelectedFile());
                Image icono = ImageIO.read(jfcSelecionaFoto.getSelectedFile()).
                        getScaledInstance(lblFoto.getWidth(),
                                lblFoto.getHeight(), Image.SCALE_DEFAULT);
                lblFoto.setIcon(new ImageIcon(icono));
                lblFoto.updateUI();
                contato.setFoto(new ImageIcon(icono));
                contato.setFotoFile(new File(jfcSelecionaFoto.getSelectedFile().getPath()));
            } catch (FileNotFoundException e) {
                JOptionPane.showMessageDialog(this, "Arquivo não encontrado!",
                    "ERRO: "+e.getMessage(), JOptionPane.ERROR_MESSAGE);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Erro de I/O.",
                    "ERRO: "+e.getMessage(), JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCarregaFotoActionPerformed

