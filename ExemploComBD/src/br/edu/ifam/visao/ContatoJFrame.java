    private void telaParaContato(Contato c) {
        if (txtCodigo.getText().trim().length()>0){
            c.setId(Long.parseLong(txtCodigo.getText()));
        }
        c.setNome(txtNome.getText());
        c.setEndereco(txtEnder.getText());
        c.setEmail(txtMail.getText());
        // Converte String para Data
        String strData = txtDtNasc.getText();
        Calendar cal = Calendar.getInstance();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            cal.setTime(sdf.parse(strData));
            c.setDataNascimento(cal);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Erro de Conversão",
                    "Informe uma data válida.", JOptionPane.ERROR_MESSAGE);
            txtDtNasc.setText("");
            txtDtNasc.requestFocus();
            ex.printStackTrace();
        }
         contato.setFoto((ImageIcon)lblFoto.getIcon());
    }

