    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        contatoDao=new ContatoDao();
        if (clicouBtnNovo) {
            // Instancio um objeto contato para poder receber os dados da tela
            contato = new Contato();
            // Passa os dados digitados para o objeto Contato
            telaParaContato(contato);
            // Adiciona o contato na tabela Contatos no banco de dados
            contatoDao.adiciona(contato);
            // Para mentar a integridade do arraylist com o banco vamos 
            // carregar o arraylist novamente. Isto foi feito devido a 
            // não termos o id do contato gerado pelo banco.
            // Poderiamos deixar isto mais econômico apenas lendo o último registro 
            // do banco e adicionando no banco de dados.
            listaDeContatos = contatoDao.getLista();
            posicao = listaDeContatos.size() - 1;
            contato = listaDeContatos.get(posicao);
            contatoDao.gravaFoto(contato);
        } else {
            // Passa os dados digitados para o objeto Contato
            telaParaContato(contato);
            contatoDao.altera(contato);
        }

        carregaDadosParaTela(contato);
        bloquearDigitacao();
        habilitarBtns();
        btnCancelar.setEnabled(false);
        btnSalvar.setEnabled(false);
    }//GEN-LAST:event_btnSalvarActionPerformed
